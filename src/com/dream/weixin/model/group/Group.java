package com.dream.weixin.model.group;

/**
 * 用户分组
 * @author lkl_wmx
 *
 */
public class Group {
	/*
	 * 分组ID
	 */
	private int id;
	/*
	 * 分组名称
	 */
	private String name;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	
}
